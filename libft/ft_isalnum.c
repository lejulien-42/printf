int		ft_isalnum(int c)
{
	int isal;
	int isnum;

	isal = 0;
	isnum = 0;
	if (((c >= 'a') && (c <= 'z')) || ((c >= 'A') && (c <= 'Z')))
		isal = 1;
	if ((c >= '0') && (c <= '9'))
		isnum = 1;
	if (isal || isnum)
		return (1);
	return (0);
}
